package com.viva.Calculator;

public class App
{
   public int add(int x,int y) {
	   return x+y;
   }
   public int sub(int x,int y) {
	   return x-y;
   }
   public int mul(int x,int y) {
	   return x*y;
   }
   public int div(int x,int y) {
	   return x/y;
   }
   public int modulusDiv(int x,int y) {
	   return x%y;
   }
}
